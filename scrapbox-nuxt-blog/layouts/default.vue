<script setup>
import { sidebarTypes } from '~/scrablog.const';

const appConfig = useAppConfig()
const sidebarRecent = appConfig
    .sidebarContents
    .find(content => content.type === sidebarTypes.RECENT_ARTICLES)

const { data: profileArticle } = await useAsyncData('default-profile', () => {
  return queryContent().where({ title: 'profile' }).findOne()
})
const { data: recentArticles } = await useAsyncData('default-recent', () => {
  return whereNotInTitle(queryContent())
    .sort({ created: -1 })
    .limit(sidebarRecent?.max ?? 5)
    .find()
})
const { data: allArticles } = await useAsyncData('default-articles', () => {
  return whereNotInTitle(queryContent()).sort({ created: -1 }).find()
})

const maxWidthClass = appConfig.showSideBar ? 'max-w-7xl' : 'max-w-3xl'
</script>

<template>
  <div class="min-h-screen text-text-base bg-bg-base">
    <!-- ヘッダー -->
    <header>
      <div class="bg-bg-content py-16">
        <div class="w-full mx-auto px-8 lg:px-16" :class="maxWidthClass">
          <h1 class="font-bold text-2xl">
            <NuxtLink to="/">{{ appConfig.blogTitle }}</NuxtLink>
          </h1>
          <p v-if="appConfig.headerText" class="my-2">{{ appConfig.headerText }}</p>
        </div>
      </div>
    </header>
    <!-- メイン -->
    <main>
      <div
        class="
          w-full flex flex-wrap items-start gap-x-16 mx-auto 
          lg:px-8 lg:flex-nowrap
        "
        :class="maxWidthClass"
      >
        <!-- 本体 -->
        <div class="w-full">
          <slot />
        </div>
        <!-- サイドバー -->
        <div
          class="
            w-full text-sm bg-bg-content mb-32 px-8
            lg:mt-32 lg:max-w-[320px]
          "
          v-if="appConfig.showSideBar"
        >
          <!-- プロフィール -->
          <div class="my-16" v-if="profileArticle">
            <h2 class="text-base font-bold my-2">プロフィール</h2>
            <Lines :lines="profileArticle.lines" />
          </div>
          <!-- 最近の記事 -->
          <div class="my-16">
            <h2 class="text-base font-bold my-2">最近の記事</h2>
            <ul>
              <li v-for="article of recentArticles" class="my-2">
                <Dot />
                <span>{{ getDateString(article.created, false) }}</span>
                <span>&nbsp;</span>
                <NuxtLink
                  :to="`/${article.id}`"
                  class="text-text-link"
                >
                  {{ article.title }}
                </NuxtLink>
              </li>
            </ul>
            <p class="my-2">
              <NuxtLink to="/history" class="text-text-link">全記事一覧</NuxtLink>
            </p>
          </div>
        </div>
      </div>
    </main>
    <!-- フッター -->
    <footer>
      <div class="text-center text-sm text-text-light py-4">
        <p v-if="appConfig.footerText" class="my-2">
          {{ appConfig.footerText }}
        </p>
        <p v-if="appConfig.showGeneratedBy" class="my-2">
          <span>Generated by </span>
          <NuxtLink
            to="https://scrapbox.io/product"
            target="_blank"
            class="underline"
          >
            Scrapbox
          </NuxtLink>
          <span> + </span>
          <NuxtLink
            to="https://github.com/kamikoloss/scrablog"
            target="_blank"
            class="underline"
          >
            Scrablog
          </NuxtLink> 
        </p>
      </div>
    </footer>
  </div>
</template>
